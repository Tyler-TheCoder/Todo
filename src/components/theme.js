export function initTheme() {
  applyTheme(getTheme());
  btn.addEventListener("click", () => {
    toggleTheme();
    applyTheme(getTheme());
  });
}

function applyTheme(theme) {
  document.body.classList.remove("dark", "light");
  document.body.classList.add(theme);
  btn.innerHTML = theme === "dark" ? moonSvg : sunSvg;
}
